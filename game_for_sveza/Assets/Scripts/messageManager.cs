        {6, "<b>Сушка шпона</b>:\nЧтобы шпон не коробился и не трескался при использовании, а также чтобы повысить прочность и долговечность, шпон сушат с помощью роликовой сушилки. В сушилке температура воздуха может достигать 180°C."},
