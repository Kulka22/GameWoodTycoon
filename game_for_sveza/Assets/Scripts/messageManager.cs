        {4, "<b>Распилка</b>:\nПосле окорки, брёвна распиливают с помощью ленточной пилорамы, дисковой пилорамы или круглопильного станка. Что делают с отходами? Опилки прессуют в паллеты (топливо) или ДСП, горбыль используют для опалубки или дров."},
