        {1, "<b>Спиливание дерева и его очистка</b>:\nХарвестер – это мощная лесная машина, которая спиливает деревья, очищает их от веток и нарезает на брёвна за один непрерывный процесс. Оператор управляет машиной из кабины, используя манипулятор."},
        {2, "<b>Замачивание брёвен</b>:\nПеред тем как делать из дерева бумагу, фанеру или мебель, брёвна часто замачивают в больших бассейнах с водой. Это помогает размягчить древесину, удалить грязь и песок, а также защитить брёвна от гниения."},
