        {8, "<b>Прессовка шпона</b>:\nС помощью пресса из шпона делают фанеру. Листы шпона укладывают слоями, чередуя направление волокон. Между укладкой на шпон наносится клей. В конченом итоге, мы получаем отличную фанеру."},
    };

    public void makeMessage(int number)
    {
        woodXP.SetActive(false);
        messageBox.SetActive(true);
        messageText.text = texts[number];
        SoundManager.PlaySound((SoundType)number,0.8f);
    }

    public void closeMessage()
    {
        messageBox.SetActive(false);
        woodXP.SetActive(true);
    }


}
